package itmo.java.basics.lab2;

public class Calculator {
    public static int sum (int operand1, int operand2){
        return operand1+operand2;
    }

    public static double sum (double operand1, double operand2){
        return operand1+operand2;
    }

    public static long sum (long operand1, long operand2){
        return operand1+operand2;
    }

    public static int division (int operand1, int operand2){
        return operand1/operand2;
    }

    public static double division (double operand1, double operand2){
        return operand1/operand2;
    }

    public static long division (long operand1, long operand2){
        return operand1/operand2;
    }

    public static int multiplication (int operand1, int operand2){
        return operand1*operand2;
    }

    public static double multiplication (double operand1, double operand2){
        return operand1*operand2;
    }

    public static long multiplication (long operand1, long operand2){
        return operand1*operand2;
    }


    public static int subtraction (int operand1, int operand2){
        return operand1-operand2;
    }

    public static double subtraction (double operand1, double operand2){
        return operand1-operand2;
    }

    public static long subtraction (long operand1, long operand2){
        return operand1-operand2;
    }
}
